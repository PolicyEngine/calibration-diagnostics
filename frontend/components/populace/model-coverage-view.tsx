// --- cluster pop-out (same mechanism as the release summary map) -------------

function ClusterPopup({
  selection,
  atRoot,
  onExpand,
  onClose,
}: {
  selection: Selection;
  atRoot: boolean;
  onExpand: () => void;
  onClose: () => void;
}) {
  const [tier, setTier] = useState<Tier>("unreached");
  const [search, setSearch] = useState("");
  const node = selection.node;
  const share = node.rules ? reachedCount(node) / node.rules : 1;

  useEffect(() => {
    const onKey = (e: KeyboardEvent) => {
      if (e.key === "Escape") onClose();
    };
    window.addEventListener("keydown", onKey);
    return () => window.removeEventListener("keydown", onKey);
  }, [onClose]);

  const q = search.trim().toLowerCase();
  const breakdown = tierBreakdown(node, tier)
    .map(({ path, names }) => ({
      path,
      names: q ? names.filter((n) => n.includes(q)) : names,
    }))
    .filter(({ names }) => names.length > 0)
    .sort((a, b) => b.names.length - a.names.length);

  return (
    <div className="flex h-full flex-col overflow-hidden rounded-xl border border-border bg-white shadow-2xl ring-1 ring-black/5">
      {/* header */}
      <div className="flex flex-wrap items-start justify-between gap-3 border-b border-border bg-muted/20 px-4 py-3">
        <div className="flex min-w-0 items-start gap-2.5">
          <span
            className="mt-1 h-3.5 w-3.5 shrink-0 rounded-full ring-1 ring-black/10"
            style={{ background: coverageColor(share) }}
          />
          <div className="min-w-0">
            <div className="font-mono text-sm font-semibold leading-tight text-foreground">
              {selection.path.join("/")}
            </div>
            <div className="mt-0.5 text-xs text-muted-foreground">
              {reachedCount(node)}/{node.rules} rules reached ({Math.round(share * 100)}%) ·{" "}
              {node.inputs} pure inputs
            </div>
          </div>
        </div>
        <div className="flex items-center gap-2">
          {(node.children?.length ?? 0) > 0 && !atRoot && (
            <button
              type="button"
              onClick={onExpand}
              className="rounded border border-primary/40 bg-primary/5 px-2 py-1 text-xs font-medium text-primary hover:bg-primary/10"
            >
              Expand cluster ↘
            </button>
          )}
          <button
            type="button"
            onClick={onClose}
            aria-label="Close cluster details"
            className="grid h-7 w-7 place-items-center rounded-md border border-border text-muted-foreground hover:bg-muted/60 hover:text-foreground"
          >
            ✕
          </button>
        </div>
      </div>

      {/* filters */}
      <div className="flex flex-wrap items-center gap-2 border-b border-border bg-white px-4 py-2.5">
        <div className="inline-flex items-center gap-1 rounded-full border border-border bg-muted/40 p-1">
          {(Object.keys(TIER_META) as Tier[]).map((k) => (
            <button
              key={k}
              type="button"
              onClick={() => setTier(k)}
              className={`h-7 rounded-full px-3 text-xs font-medium transition-all ${
                tier === k
                  ? "bg-white text-foreground shadow-sm ring-1 ring-black/5"
                  : "text-muted-foreground hover:text-foreground"
              }`}
            >
              {TIER_META[k].label} · {fmt(tierCount(node, k), { digits: 0 })}
            </button>
          ))}
        </div>
        <input
          type="search"
          value={search}
          placeholder="Filter variables…"
          onChange={(e) => setSearch(e.target.value)}
          className="h-8 w-44 rounded-md border border-border bg-white px-2.5 text-sm focus:border-primary/60 focus:outline-none"
        />
        <span className="text-[11px] text-muted-foreground">{TIER_META[tier].blurb}</span>
      </div>

      {/* body */}
      <div className="min-h-0 flex-1 overflow-y-auto px-4 py-3">
        {!breakdown.length ? (
          <div className="py-8 text-center text-sm text-muted-foreground">
            {q
              ? `No ${TIER_META[tier].label.toLowerCase()} variables matching “${search}” here.`
              : `No ${TIER_META[tier].label.toLowerCase()} variables under this path.`}
          </div>
        ) : (
          <div className="flex flex-col gap-3">
            {breakdown.map(({ path, names }) => (
              <div key={path || "."}>
                <div className="flex items-baseline gap-2">
                  <span className="font-mono text-xs font-semibold text-foreground">
                    {path ? `${selection.path.join("/")}/${path}` : selection.path.join("/")}
                  </span>
                  <span className="text-xs text-muted-foreground">
                    {names.length} {TIER_META[tier].label.toLowerCase()}
                  </span>
                </div>
                <div className="mt-0.5 flex flex-wrap gap-1">
                  {names.map((name) => (
                    <a
                      key={name}
                      href={`https://github.com/PolicyEngine/policyengine-us/search?q=${name}&type=code`}
                      target="_blank"
                      rel="noreferrer"
                      className={`rounded border px-1.5 py-0.5 font-mono text-[10px] ${TIER_META[tier].chip}`}
                    >
                      {name}
                    </a>
                  ))}
                </div>
              </div>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}

// --- main view ----------------------------------------------------------------

export function ModelCoverageView({ initialPath = "" }: { initialPath?: string }) {
  const [data, setData] = useState<CoverageData | null>(null);
  const [error, setError] = useState<string | null>(null);
  const [root, setRoot] = useState<string[]>(
    initialPath ? initialPath.split("/").filter(Boolean) : [],
  );
  const [selected, setSelected] = useState<Selection | null>(null);
  const [hovered, setHovered] = useState<string | null>(null);
  const containerRef = useRef<HTMLDivElement>(null);
  const [width, setWidth] = useState(1100);

  useEffect(() => {
    fetch(withBasePath("/model-coverage.json"))
      .then((r) => (r.ok ? r.json() : Promise.reject(new Error(`HTTP ${r.status}`))))
      .then(setData)
      .catch((e) => setError(String(e)));
  }, []);

  // Depends on `data`: the container only renders once loading finishes, so
  // observing on first mount would attach to nothing and the map would stay
  // at its fallback width.
  useEffect(() => {
    const el = containerRef.current;
    if (!el) return;
    setWidth(Math.round(el.getBoundingClientRect().width));
    const observer = new ResizeObserver((entries) => {
      const w = entries[0]?.contentRect.width;
      if (w) setWidth(Math.round(w));
    });
    observer.observe(el);
    return () => observer.disconnect();
  }, [data]);

  // An unknown path (stale link, typo) falls back to the whole tree.
  const resolvedRoot = useMemo(
    () => (data && findNode(data.tree, root) ? root : []),
    [data, root],
  );
  const rootNode = useMemo(
    () => (data ? findNode(data.tree, resolvedRoot) : null),
    [data, resolvedRoot],
  );

  const height = Math.round(Math.min(Math.max(width * 0.52, 420), 620));

  const laid = useMemo(() => {
    if (!rootNode) return [];
    const groups = groupsFor(rootNode, resolvedRoot);
    const placedGroups = squarify(
      groups.map((g) => ({ value: g.node.rules, data: g })),
      { x: 0, y: 0, w: width, h: height },
    );
    return placedGroups.map((pg) => {
      const pad = 3;
      const headerH = pg.h >= 56 && pg.w >= 80 ? 18 : 0;
      const inner = {
        x: pg.x + pad,
        y: pg.y + pad + headerH,
        w: Math.max(pg.w - pad * 2, 0),
        h: Math.max(pg.h - pad * 2 - headerH, 0),
      };
      const grandchildren = (pg.data.node.children ?? []).filter((c) => c.rules > 0);
      const groupDirect = directNode(pg.data.node);
      const leaves = squarify(
        grandchildren.length
          ? [
              ...grandchildren.map((c) => ({
                value: c.rules,
                data: { node: c, path: [...pg.data.path, c.name] },
              })),
              ...(groupDirect
                ? [
                    {
                      value: groupDirect.rules,
                      data: { node: groupDirect, path: [...pg.data.path, "(direct)"] },
                    },
                  ]
                : []),
            ]
          : [{ value: pg.data.node.rules, data: { node: pg.data.node, path: pg.data.path } }],
        inner,
      );
      return { group: pg, headerH, leaves };
    });
  }, [rootNode, resolvedRoot, width, height]);

  if (error) return <EmptyState title="Coverage data unavailable" description={error} />;
  if (!data) return <LoadingBlock label="Loading model coverage…" />;

  const t = data.totals;
  const reached = t.anchored + t.exercised;
  const sectionOptions = [
    { value: "", label: "All sections" },
    ...groupsFor(data.tree, [])
      .filter((g) => !g.synthetic)
      .map((g) => ({ value: g.path.join("/"), label: g.label })),
  ];

  function drillTo(path: string[]) {
    setRoot(path);
    setSelected(null);
  }

  return (
    <div className="flex flex-col gap-5">
      <PageHeader
        eyebrow="Populace · rules engine"
        title="Validation reach"
        description={
          <>
            Which rule variables in{" "}
            <a
              className="underline decoration-dotted underline-offset-2"
              href="https://github.com/PolicyEngine/policyengine-us"
              target="_blank"
              rel="noreferrer"
            >
              policyengine-us
            </a>{" "}
            (v{data.source.version}) this dashboard&apos;s validation would notice
            breaking. <span className="font-medium text-foreground">Anchored</span> rules
            are directly compared to an official number — a calibration target (
            {fmt(data.anchor_counts.calibration, { digits: 0 })} variables), a JCT reform
            score ({fmt(data.anchor_counts.reform, { digits: 0 })}), or the IRS SOI
            backtest ({fmt(data.anchor_counts.backtest, { digits: 0 })}).{" "}
            <span className="font-medium text-foreground">Exercised</span> rules feed an
            anchored number, so a break moves a checked figure (static dependency scan —
            an approximation). Everything else is{" "}
            <span className="font-medium text-foreground">unreached</span>: it could be
            wrong and nothing here would move. Upstream unit tests are deliberately not
            counted.
          </>
        }
      />

      <div className="grid grid-cols-2 gap-3 md:grid-cols-4">
        <KpiCard
          label="Rule variables"
          value={fmt(t.rules, { digits: 0 })}
          hint={`+ ${fmt(t.inputs, { digits: 0 })} pure inputs`}
        />
        <KpiCard
          label="Anchored"
          value={fmt(t.anchored, { digits: 0 })}
          hint="directly checked vs official figures"
        />
        <KpiCard
          label="Reached"
          value={fmt(reached / t.rules, { pct: true, digits: 1 })}
          hint={`${fmt(t.exercised, { digits: 0 })} more feed a checked number`}
        />
        <KpiCard
          label="Unreached rules"
          value={fmt(t.unreached, { digits: 0 })}
          hint="invisible to every dashboard check"
        />
      </div>

      <SectionCard
        title="Validation reach map"
        description="Tile area is the number of rule variables in that part of the tree; color is the share reached by dashboard validation — same scale as the calibration map: green means watched, amber and rose are blind spots. Click a tile to pop out its covered / uncovered variables; expand from there (or click a section header) to zoom in."
        actions={
          <ToolbarSelect
            label="Section"
            value={resolvedRoot.join("/")}
            onChange={(v) => drillTo(v ? v.split("/") : [])}
            options={sectionOptions}
          />
        }
      >
        {resolvedRoot.length > 0 && (
          <div className="mb-2 flex flex-wrap items-center gap-1 text-xs">
            <button
              type="button"
              onClick={() => drillTo([])}
              className="rounded border border-border px-1.5 py-0.5 text-muted-foreground hover:bg-muted/40 hover:text-foreground"
            >
              All
            </button>
            {resolvedRoot.map((part, i) => (
              <span key={`${part}-${i}`} className="flex items-center gap-1">
                <span className="text-muted-foreground">/</span>
                <button
                  type="button"
                  onClick={() => drillTo(resolvedRoot.slice(0, i + 1))}
                  className={`rounded border px-1.5 py-0.5 ${
                    i === resolvedRoot.length - 1
                      ? "border-primary/40 bg-primary/5 font-semibold text-foreground"
                      : "border-border text-muted-foreground hover:bg-muted/40 hover:text-foreground"
                  }`}
                >
                  {part}
                </button>
              </span>
            ))}
          </div>
        )}

        <div ref={containerRef} className="relative w-full" style={{ height }}>
          {laid.map(({ group, headerH, leaves }) => (
            <div key={group.data.label}>
              <div
                className="absolute rounded-md border border-border/80 bg-slate-50"
                style={{
                  left: group.x + 1.5,
                  top: group.y + 1.5,
                  width: Math.max(group.w - 3, 0),
                  height: Math.max(group.h - 3, 0),
                }}
              />
              {headerH > 0 && (
                <button
                  type="button"
                  onClick={() =>
                    group.data.node.children?.length
                      ? drillTo(group.data.path)
                      : setSelected({ path: group.data.path, node: group.data.node })
                  }
                  title={
                    group.data.node.children?.length
                      ? `Zoom into ${group.data.label}`
                      : group.data.label
                  }
                  className="absolute flex items-baseline gap-1.5 overflow-hidden text-left"
                  style={{ left: group.x + 8, top: group.y + 3, width: group.w - 12, height: headerH }}
                >
                  <span className="truncate text-[11px] font-semibold tracking-tight text-foreground hover:underline">
                    {group.data.label}
                  </span>
                  <span className="shrink-0 font-mono text-[10px] text-muted-foreground">
                    {Math.round((reachedCount(group.data.node) / group.data.node.rules) * 100)}%
                    {group.data.node.children?.length ? " ↘" : ""}
                  </span>
                </button>
              )}
              {leaves.map((leaf) => {
                const n = leaf.data.node;
                const leafPath = leaf.data.path.join("/");
                const share = n.rules ? reachedCount(n) / n.rules : 1;
                const w = Math.max(leaf.w - 2, 0);
                const h = Math.max(leaf.h - 2, 0);
                if (w < 4 || h < 4) return null;
                const isSelected = selected?.path.join("/") === leafPath;
                const isHover = hovered === leafPath;
                const showText = w >= 46 && h >= 24;
                return (
                  <button
                    type="button"
                    key={leafPath}
                    aria-label={`${leafPath}, ${reachedCount(n)} of ${n.rules} rules reached`}
                    aria-pressed={isSelected}
                    title={`${leafPath} — ${reachedCount(n)}/${n.rules} rules reached (${Math.round(share * 100)}%) — click for details`}
                    onClick={() => setSelected({ path: leaf.data.path, node: n })}
                    onMouseEnter={() => setHovered(leafPath)}
                    onMouseLeave={() => setHovered(null)}
                    className="group absolute flex flex-col overflow-hidden px-1.5 py-1 text-left outline-none transition-[transform,box-shadow,opacity] duration-150 focus-visible:ring-2 focus-visible:ring-primary focus-visible:ring-offset-1"
                    style={{
                      left: leaf.x + 1,
                      top: leaf.y + 1,
                      width: w,
                      height: h,
                      borderRadius: Math.max(1.5, Math.min(5, Math.min(w, h) * 0.25)),
                      background: coverageColor(share),
                      color: coverageTextColor(share),
                      opacity: selected && !isSelected ? 0.65 : isHover ? 1 : 0.92,
                      zIndex: isSelected ? 11 : isHover ? 10 : 1,
                      transform: isHover || isSelected ? "scale(1.015)" : "scale(1)",
                      boxShadow: isSelected
                        ? "0 0 0 2px #ffffff, 0 0 0 4px #319795, 0 8px 22px -6px rgba(15,23,42,0.35)"
                        : isHover
                          ? "0 6px 20px -4px rgba(15,23,42,0.3), inset 0 0 0 1px rgba(255,255,255,0.25)"
                          : "inset 0 0 0 1px rgba(255,255,255,0.08)",
                    }}
                  >
                    {showText && (
                      <>
                        <span className="truncate text-[10px] font-semibold leading-tight">
                          {n.name}
                        </span>
                        <span className="truncate text-[9px] leading-tight opacity-85">
                          {reachedCount(n)}/{n.rules}
                        </span>
                      </>
                    )}
                  </button>
                );
              })}
            </div>
          ))}

          {/* cluster detail pops out over the graph, like the calibration map */}
          {selected && (
            <div className="absolute inset-0 z-30">
              <style>{`@keyframes scrimIn{from{opacity:0}to{opacity:1}}@keyframes popIn{from{opacity:0;transform:scale(0.97)}to{opacity:1;transform:none}}`}</style>
              <button
                type="button"
                aria-label="Close cluster details"
                onClick={() => setSelected(null)}
                className="absolute inset-0 cursor-default bg-slate-900/20 backdrop-blur-[2px] motion-safe:animate-[scrimIn_140ms_ease-out]"
              />
              <div className="absolute inset-2 motion-safe:animate-[popIn_180ms_cubic-bezier(0.16,1,0.3,1)] sm:inset-3">
                <ClusterPopup
                  key={selected.path.join("/")}
                  selection={selected}
                  atRoot={selected.path.join("/") === resolvedRoot.join("/")}
                  onExpand={() => drillTo(selected.path)}
                  onClose={() => setSelected(null)}
                />
              </div>
            </div>
          )}
        </div>

        <div className="mt-2 flex items-center gap-3 text-[11px] text-muted-foreground">
          <span>Reached:</span>
          {[
            [1.0, "100%"],
            [0.9, "90%"],
            [0.75, "75%"],
            [0.5, "50%"],
            [0.25, "25%"],
            [0.0, "0%"],
          ].map(([v, label]) => (
            <span key={String(label)} className="inline-flex items-center gap-1">
              <span
                className="inline-block h-3 w-3 rounded-sm border border-border/60"
                style={{ background: coverageColor(v as number) }}
              />
              {label}
            </span>
          ))}
        </div>
      </SectionCard>

      <SectionCard
        title="Largest unreached pockets"
        description="Leaf directories with the most rule variables no dashboard check would catch — the concentrated unknown-unknowns."
        padded={false}
      >
        <table className="w-full text-left text-sm">
          <thead>
            <tr className="border-b border-border text-[11px] uppercase tracking-wider text-muted-foreground">
              <th className="px-3 py-2 font-semibold">Path</th>
              <th className="px-3 py-2 text-right font-semibold">Rules</th>
              <th className="px-3 py-2 text-right font-semibold">Reached</th>
              <th className="px-3 py-2 text-right font-semibold">Unreached</th>
              <th className="px-3 py-2 font-semibold">Unreached variables</th>
            </tr>
          </thead>
          <tbody>
            {worstPockets(data.tree).map(({ path, rules, reached: pocketReached, unreached }) => (
              <tr key={path} className="border-b border-border/60 align-top last:border-b-0">
                <td className="px-3 py-2 font-mono text-xs">{path.replace(/^variables\//, "")}</td>
                <td className="px-3 py-2 text-right tabular-nums">{rules}</td>
                <td className="px-3 py-2 text-right tabular-nums">{pocketReached}</td>
                <td className="px-3 py-2 text-right font-medium tabular-nums text-rose-700">
                  {unreached.length}
                </td>
                <td className="max-w-xl px-3 py-2 font-mono text-[11px] leading-snug text-muted-foreground">
                  {unreached.slice(0, 8).join(", ")}
                  {unreached.length > 8 ? ", …" : ""}
                </td>
              </tr>
            ))}
          </tbody>
        </table>
      </SectionCard>
    </div>
  );
}
