// ---------------------------------------------------------------------------
// Which policyengine-us rule variables the dashboard's own validation would
// notice breaking. Tiers: anchored (directly compared to an official number —
// a calibration target, reform score, or SOI backtest line), exercised
// (transitively feeds an anchored variable, via a static dependency scan), and
// unreached (invisible to every check — the unknown unknowns).
// The map mirrors the release summary's calibration map: HTML button tiles
// with a pop-out cluster panel over the graph.
// Data: public/model-coverage.json, regenerated by
// scripts/generate_model_coverage.py against policyengine-us + populace
// checkouts and the live release artifact.
// ---------------------------------------------------------------------------

type Tier = "unreached" | "exercised" | "anchored";

interface CoverageNode {
  name: string;
  rules: number;
  anchored: number;
  exercised: number;
  inputs: number;
  children?: CoverageNode[];
  anchored_names?: string[];
  exercised_names?: string[];
  unreached_names?: string[];
}

interface CoverageData {
  source: { repo: string; version: string; commit: string };
  release_id?: string | null;
  anchor_counts: { calibration: number; reform: number; backtest: number };
  totals: {
    variables: number;
    rules: number;
    anchored: number;
    exercised: number;
    unreached: number;
    inputs: number;
  };
  tree: CoverageNode;
}

function reachedCount(n: CoverageNode): number {
  return n.anchored + n.exercised;
}

function unreachedIn(n: CoverageNode): number {
  return n.rules - reachedCount(n);
}

function tierCount(n: CoverageNode, tier: Tier): number {
  if (tier === "anchored") return n.anchored;
  if (tier === "exercised") return n.exercised;
  return unreachedIn(n);
}

function findNode(tree: CoverageNode, path: string[]): CoverageNode | null {
  let cur = tree;
  for (const part of path) {
    const next = (cur.children ?? []).find((c) => c.name === part);
    if (!next) return null;
    cur = next;
  }
  return cur;
}

// --- tiny squarified treemap ------------------------------------------------

interface Rect {
  x: number;
  y: number;
  w: number;
  h: number;
}

interface Sized<T> {
  value: number;
  data: T;
}

type Placed<T> = Rect & { data: T };

function squarify<T>(items: Sized<T>[], rect: Rect): Placed<T>[] {
  const sorted = [...items].sort((a, b) => b.value - a.value);
  const total = sorted.reduce((s, i) => s + i.value, 0);
  if (!total) return [];
  const out: Placed<T>[] = [];
  let rest = { ...rect };
  let restValue = total;
  let row: Sized<T>[] = [];

  const worst = (r: Sized<T>[], side: number, area: number): number => {
    const sum = r.reduce((s, i) => s + i.value, 0) * (area / restValue);
    const max = Math.max(...r.map((i) => i.value)) * (area / restValue);
    const min = Math.min(...r.map((i) => i.value)) * (area / restValue);
    const s2 = sum * sum;
    return Math.max((side * side * max) / s2, s2 / (side * side * min));
  };

  const layoutRow = (r: Sized<T>[]) => {
    const area = rest.w * rest.h;
    const sum = r.reduce((s, i) => s + i.value, 0) * (area / restValue);
    const horizontal = rest.w >= rest.h;
    const side = horizontal ? rest.h : rest.w;
    const thickness = sum / side;
    let offset = 0;
    for (const item of r) {
      const len = (item.value * (area / restValue)) / thickness;
      out.push(
        horizontal
          ? { x: rest.x, y: rest.y + offset, w: thickness, h: len, data: item.data }
          : { x: rest.x + offset, y: rest.y, w: len, h: thickness, data: item.data },
      );
      offset += len;
    }
    restValue -= r.reduce((s, i) => s + i.value, 0);
    rest = horizontal
      ? { x: rest.x + thickness, y: rest.y, w: rest.w - thickness, h: rest.h }
      : { x: rest.x, y: rest.y + thickness, w: rest.w, h: rest.h - thickness };
  };

  for (const item of sorted) {
    const side = Math.min(rest.w, rest.h);
    const area = rest.w * rest.h;
    if (!row.length || worst([...row, item], side, area) <= worst(row, side, area)) {
      row.push(item);
    } else {
      layoutRow(row);
      row = [item];
    }
  }
  if (row.length) layoutRow(row);
  return out;
}

// --- coverage color ----------------------------------------------------------

// Same fit-scale ramp as the calibration map (teal-green → sage → amber →
// rose). Reached share is mapped onto the scale's error axis so both maps
// share one visual language: green = validated, warm = blind spots.
const SHARE_TO_ERROR: [number, number][] = [
  [1.0, 0.0],
  [0.9, 0.03],
  [0.75, 0.06],
  [0.5, 0.1],
  [0.25, 0.2],
  [0.0, 0.4],
];

function coverageError(share: number): number {
  const s = Math.min(Math.max(share, 0), 1);
  if (s >= SHARE_TO_ERROR[0][0]) return SHARE_TO_ERROR[0][1];
  for (let i = 1; i < SHARE_TO_ERROR.length; i += 1) {
    const [hi, eHi] = SHARE_TO_ERROR[i - 1];
    const [lo, eLo] = SHARE_TO_ERROR[i];
    if (s >= lo) {
      const t = (hi - s) / (hi - lo);
      return eHi + (eLo - eHi) * t;
    }
  }
  return SHARE_TO_ERROR[SHARE_TO_ERROR.length - 1][1];
}

function coverageColor(share: number): string {
  return fitColor(coverageError(share));
}

function coverageTextColor(share: number): string {
  return readableInk(coverageError(share));
}

// --- grouping ----------------------------------------------------------------

interface Group {
  label: string;
  path: string[];
  node: CoverageNode;
  synthetic?: boolean;
}

// A directory can hold .py files directly AND subdirectories (390 do). The
// direct files are part of the node's rolled-up counts but belong to no child,
// so they get a synthetic "(direct)" tile to stay visible when drilling.
function directNode(n: CoverageNode): CoverageNode | null {
  if (!n.children?.length) return null;
  const a = n.anchored_names ?? [];
  const e = n.exercised_names ?? [];
  const u = n.unreached_names ?? [];
  const rules = a.length + e.length + u.length;
  if (!rules) return null;
  return {
    name: "(direct)",
    rules,
    anchored: a.length,
    exercised: e.length,
    inputs: 0,
    anchored_names: n.anchored_names,
    exercised_names: n.exercised_names,
    unreached_names: n.unreached_names,
  };
}

// At the tree root, promote gov's children so groups are gov/irs, gov/states…;
// when drilled into a cluster, groups are simply its children.
function groupsFor(node: CoverageNode, path: string[]): Group[] {
  const groups: Group[] = [];
  for (const child of node.children ?? []) {
    if (!path.length && child.name === "gov" && child.children?.length) {
      for (const g of child.children) {
        groups.push({ label: `gov/${g.name}`, path: ["gov", g.name], node: g });
      }
      const gd = directNode(child);
      if (gd) {
        groups.push({ label: "gov (direct)", path: ["gov", "(direct)"], node: gd, synthetic: true });
      }
    } else {
      groups.push({ label: child.name, path: [...path, child.name], node: child });
    }
  }
  const dn = directNode(node);
  if (dn) groups.push({ label: "(direct)", path: [...path, "(direct)"], node: dn, synthetic: true });
  return groups.filter((g) => g.node.rules > 0);
}

function collectTierNames(node: CoverageNode, tier: Tier): string[] {
  return [
    ...(node[`${tier}_names`] ?? []),
    ...(node.children ?? []).flatMap((c) => collectTierNames(c, tier)),
  ];
}

// Aggregate pockets at directory depth 4 (gov/states/mt/tax) so the table
// shows meaningful program-sized clusters, not fragmented deep leaves.
interface Pocket {
  path: string;
  rules: number;
  reached: number;
  unreached: string[];
}

function worstPockets(tree: CoverageNode): Pocket[] {
  const pockets: Pocket[] = [];
  const walk = (n: CoverageNode, path: string, depth: number) => {
    if (depth < 4 && n.children?.length) {
      for (const c of n.children) walk(c, `${path}/${c.name}`, depth + 1);
      // Files living directly in this directory belong to no child — surface
      // them as their own pocket so they are not silently skipped.
      const own = n.unreached_names ?? [];
      if (own.length) {
        const ownRules =
          (n.anchored_names?.length ?? 0) + (n.exercised_names?.length ?? 0) + own.length;
        pockets.push({ path, rules: ownRules, reached: ownRules - own.length, unreached: own });
      }
      return;
    }
    const unreached = collectTierNames(n, "unreached");
    if (unreached.length > 0) {
      pockets.push({ path, rules: n.rules, reached: reachedCount(n), unreached });
    }
  };
  walk(tree, tree.name, 0);
  return pockets.sort((a, b) => b.unreached.length - a.unreached.length).slice(0, 15);
}

interface Selection {
  path: string[];
  node: CoverageNode;
}

// Descendant directories of a node that hold names of the given tier.
function tierBreakdown(
  node: CoverageNode,
  tier: Tier,
  path = "",
): { path: string; names: string[] }[] {
  const own = node[`${tier}_names`] ?? [];
  const nested = (node.children ?? []).flatMap((c) =>
    tierBreakdown(c, tier, path ? `${path}/${c.name}` : c.name),
  );
  return own.length ? [{ path, names: own }, ...nested] : nested;
}

